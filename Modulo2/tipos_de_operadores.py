#Adição: +
print(1 + 1)

#subtração: -
print(10 - 2)

#multiplicação:
print(4 * 3)

#divisão: / (sempre float)

print(12 / 3)

#divisão inteira: //

print(12 // 3)

#Módulo (resto da divisão): %

print(10 % 3)

# Exponenciação: **

print( 2 ** 3)

#ordem dos operadores:

# 1: Todo valor dentro dos parêntsis
# 2: Expoêntes (**)
# 3: Multiplicações e divisões (*, /)
# 4: Soma e subtração (+, -)

#exemplos:

print(10 ** 2 * 2)
#--> 200

print (10 ** (2*2))
#10000 porque 2*2 é realizado primeiro, ficando 10 elevado a 4.


