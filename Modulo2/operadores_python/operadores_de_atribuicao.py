# tipos de atribuições:

#simples:
saldo = 500

#o valor de 500 foi atribuido à 500.

#atribuição de adição
saldo += 200


#atribuição de subtração:
saldo1 = 200
saldo1 -= 200

#divisão:

saldo2 = 200
saldo2 /= 5

#multiplicação:

saldo3 = 200
saldo3 *= 2

#divisão inteira
saldo4 = 200
saldo4 //= 5

#modular:
saldo5 = 200
saldo5 %=480

#exponencial:
saldo6 = 200
saldo6 **= 2