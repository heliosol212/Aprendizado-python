#Operadores de associação

#In

frutas = ["maça", "banana"]
vegetais = ["batata", "cenoura"]

print("maça" in frutas)
#"In" procura certos valores dentro do que foi especificado, no caso, foi perguntado se a string 'maça' estava dentro da lista 'frutas'.
#Operadores de negação também podem ser utilizados

print("morango" not in vegetais)

#a string "morango" nao está em "vegetais".