
#igualdade: =/== 

saldo = 450
saque = 200

print("saldo é igual a saque?")
print(saldo == saque)
#saldo é igual a saque?
#--> falso

#Diferente: !=

print("saldo é diferente de saque?")
print(saldo != saque)
#saldo é diferente de saque?
#--> verdadeiro


#maior que/maior ou igual:

#maior que
print("saldo é maior que saque?")
print(saldo > saque)

#maior ou igual
print("saldo é maior ou igual a saque?")
print(saldo >= saque)


#menor que/menor ou igual:

#menor que
print("saldo é menor que saque?")
print(saldo < saque)

#menor ou igual
print("saldo é menor ou igual a saque?")
print(saldo <= saque)
